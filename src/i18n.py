def translate(lang: str, key: str, **kwargs: object) -> str:
    """Translate a key using the requested language with German fallback."""
    catalog = TRANSLATIONS.get(lang, TRANSLATIONS["de"])
    text = catalog.get(key) or TRANSLATIONS["de"].get(key) or key
    if not kwargs:
        return text
    return _PLACEHOLDER_PATTERN.sub(
        lambda match: str(kwargs.get(match.group(1), match.group(0))),
        text,
    )


